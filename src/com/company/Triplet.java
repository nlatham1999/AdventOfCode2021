package com.company;

public class Triplet {

    public int first;
    public int second;
    public int third;

    public Triplet(int first, int second, int third){
        this.first = first;
        this.second = second;
        this.third = third;
    }

    public Triplet(){
        this.first = 0;
        this.second = 0;
        this.third = 0;
    }
}
